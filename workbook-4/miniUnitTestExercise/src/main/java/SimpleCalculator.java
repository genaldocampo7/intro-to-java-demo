public class SimpleCalculator {
    public double addTwoPositiveNumbers(double x, double y) {
        return x + y;
    }

    public double addTwoNegativeNumbers (double x, double y) {
        return x + y;
    }


    public double addPositive_NegativeNumbers (double x, double y) {
        return x + y;
    }

    public double divideTwoNumbers(double x, double y) {
        if (y != 0) {
            return x / y;
        } else {
            return 0;
            
        }
    }
}
